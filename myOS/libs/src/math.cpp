// ============================================================================
// BASIC MATH FUNCTIONS
// ============================================================================

int abs(int x) {
    return x < 0 ? -x : x;
}

double fabs(double x) {
    return x < 0.0 ? -x : x;
}

int min(int a, int b) {
    return a < b ? a : b;
}

int max(int a, int b) {
    return a > b ? a : b;
}

int clamp(int value, int min_val, int max_val) {
    if (value < min_val) return min_val;
    if (value > max_val) return max_val;
    return value;
}

// ============================================================================
// POWER AND ROOTS
// ============================================================================

int ipow(int base, int exp) {
    int result = 1;
    while (exp > 0) {
        if (exp & 1) {
            result *= base;
        }
        base *= base;
        exp >>= 1;
    }
    return result;
}

double sqrt(double x) {
    if (x < 0) return -1.0;  // Error: negative input
    if (x == 0) return 0;
    
    // Newton-Raphson method
    double guess = x / 2.0;
    double epsilon = 0.00001;
    
    for (int i = 0; i < 100; i++) {
        double next = (guess + x / guess) / 2.0;
        if (fabs(next - guess) < epsilon) {
            return next;
        }
        guess = next;
    }
    
    return guess;
}

